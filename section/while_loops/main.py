start_number = 5
countdown_values = []

while start_number > 0:
    # Append start_number to the list "countdown_values":
    countdown_values.append(start_number)
    
    # Print the list "countdown_values":
    print(f"Discount countdown complete! {countdown_values}")

    #Decrease the start_number by 1 for the next loop:
    start_number -= 1
    

    